package erp;

public class HolidayBean {
	private int holi_idx;
	private String holi_date;
	private int emp_num;
	private String emp_dept;
	private String emp_name;
	private String holi_content;
	private String holi_state;
	
	public int getHoli_idx() {
		return holi_idx;
	}
	public void setHoli_idx(int holi_idx) {
		this.holi_idx = holi_idx;
	}
	public String getHoli_date() {
		return holi_date;
	}
	public void setHoli_date(String holi_date) {
		this.holi_date = holi_date;
	}
	public int getEmp_num() {
		return emp_num;
	}
	public void setEmp_num(int emp_num) {
		this.emp_num = emp_num;
	}
	public String getEmp_dept() {
		return emp_dept;
	}
	public void setEmp_dept(String emp_dept) {
		this.emp_dept = emp_dept;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getHoli_content() {
		return holi_content;
	}
	public void setHoli_content(String holi_content) {
		this.holi_content = holi_content;
	}
	public String getHoli_state() {
		return holi_state;
	}
	public void setHoli_state(String holi_state) {
		this.holi_state = holi_state;
	}
	
}
