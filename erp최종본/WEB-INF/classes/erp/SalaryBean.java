package erp;

public class SalaryBean {
	private String salary_num;
	private String salary_name;
	private String salary_dept;
	private String salary_empname;
	private String salary_job;
	private String salary_date;
	private String salary_content;
	private String salary_state;
	
	public String getSalary_num() {
		return salary_num;
	}
	public void setSalary_num(String salary_num) {
		this.salary_num = salary_num;
	}
	public String getSalary_name() {
		return salary_name;
	}
	public void setSalary_name(String salary_name) {
		this.salary_name = salary_name;
	}
	public String getSalary_dept() {
		return salary_dept;
	}
	public void setSalary_dept(String salary_dept) {
		this.salary_dept = salary_dept;
	}
	public String getSalary_empname() {
		return salary_empname;
	}
	public void setSalary_empname(String salary_empname) {
		this.salary_empname = salary_empname;
	}
	public String getSalary_job() {
		return salary_job;
	}
	public void setSalary_job(String salary_job) {
		this.salary_job = salary_job;
	}
	public String getSalary_date() {
		return salary_date;
	}
	public void setSalary_date(String salary_date) {
		this.salary_date = salary_date;
	}
	public String getSalary_content() {
		return salary_content;
	}
	public void setSalary_content(String salary_content) {
		this.salary_content = salary_content;
	}
	public String getSalary_state() {
		return salary_state;
	}
	public void setSalary_state(String salary_state) {
		this.salary_state = salary_state;
	}
	
}